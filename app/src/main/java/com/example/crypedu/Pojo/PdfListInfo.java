package com.example.crypedu.Pojo;

public class PdfListInfo {

    private String id;
    private String worksheet_url;
    private String chapter_name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getWorksheet_url() {
        return worksheet_url;
    }

    public void setWorksheet_url(String worksheet_url) {
        this.worksheet_url = worksheet_url;
    }

    public String getChapter_name() {
        return chapter_name;
    }

    public void setChapter_name(String chapter_name) {
        this.chapter_name = chapter_name;
    }


}
